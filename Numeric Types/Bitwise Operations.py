x = 8  # 1000 in binary

# Right shift (divide by 2)
print(x >> 1)  # 4 (0100)

# Left shift (multiply by 2)
print(x << 1)  # 16 (10000)

# Bitwise AND
print(5 & 3)  # 1 (0101 & 0011 = 0001)

# Bitwise OR
print(5 | 3)  # 7 (0101 | 0011 = 0111)

# Explanation:

# << shifts bits left (multiplication).
# >> shifts bits right (division).
# & and | perform bitwise AND/OR.
# Example Output:

# 4
# 16
# 1
# 7
