
# True division
print(10 / 3)   # 3.333...

# Floor division
print(10 // 3)  # 3 (truncates decimal part)

# Negative floor division
print(-10 // 3) # -4 (rounds down)

# Explanation:

# / always returns a float.
# // truncates towards negative infinity.
# Example Output:

# 3.3333333333333335
# 3
# -4
