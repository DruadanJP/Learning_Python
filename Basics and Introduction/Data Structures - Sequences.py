# String operations
text = "Python"
print(text[0], text[-1])  # Access first and last character

# List operations
numbers = [10, 20, 30, 40]
numbers.append(50)  # Add an item
print(numbers)

# Tuple (Immutable sequence)
coordinates = (3, 4)
print(f"X: {coordinates[0]}, Y: {coordinates[1]}")

