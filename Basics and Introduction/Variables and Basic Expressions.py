# Variable assignment
x = 10
y = 5

# Basic expressions
sum_result = x + y
diff_result = x - y
prod_result = x * y
quot_result = x / y

# Display results
print(f"Sum: {sum_result}, Difference: {diff_result}")
print(f"Product: {prod_result}, Quotient: {quot_result:.2f}")

# Explanation:
# 
# Variables are created dynamically when assigned a value.
# Python replaces variables with their values during expressions.
# No need for explicit type declaration.
# Example Output:

# Sum: 15, Difference: 5
# Product: 50, Quotient: 2.00